        <h1>¿Qué es un agujero negro?</h1>
        <br>
        <p>Un agujero negro1​ es una región finita del espacio en cuyo interior existe una concentración de masa lo suficientemente elevada como para generar un <br>campo gravitatorio tal que ninguna partícula material, ni siquiera la luz, puede escapar de ella. Sin embargo, los agujeros negros pueden ser capaces de <br>emitir un tipo de radiación, la radiación de Hawking, conjeturada por Stephen Hawking en la década de 1970. La radiación emitida por agujeros negros<br>como Cygnus X-1 no procede del propio agujero negro sino de su disco de acreción.</p>
        <br>
        <img id=image1 src="image/AN1.jpg">
        <br>
        <p>La gravedad de un agujero negro, o «curvatura del espacio-tiempo», provoca una singularidad envuelta por una superficie cerrada, llamada horizonte <br>de eventos. Esto es previsto por las ecuaciones del campo de Einstein. El horizonte de sucesos separa la región del agujero negro del resto del universo,<br> y a partir de él ninguna partícula puede salir, incluidos los fotones. Dicha curvatura es estudiada por la relatividad general, la que predijo la existencia<br> de los agujeros negros y fue su primer indicio. </p>
        <br>

